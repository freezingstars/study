import turtle
turtle.screensize(600, 600,'white')
turtle.setup(width=600, height=600)
turtle.shape('turtle')
turtle.colormode(255.0)
turtle.fillcolor(0,245,255)
turtle.speed(10)
turtle.pensize(3)
turtle.penup()
turtle.goto(-60,0)
turtle.pendown()
circle_degree = (450,540,450,540,360)
forward_degree = (0,180,180,90,180)
circle_color = ('blue','black','red','green','yellow')
for i in range(5):
    turtle.pencolor(circle_color[i])
    turtle.left(forward_degree[i])
    turtle.circle(30,circle_degree[i])
turtle.done()
